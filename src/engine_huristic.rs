// -----------------------------------------------------------------------------
// 휴리스틱 모듈: evaluate 및 score_move에 사용되는 순수 휴리스틱 함수들.
//
// 기물 가치 단위: 센티폰(centipawn). 폰=100, 나이트=320, 비숍=330, 룩=500, 퀸=900.
// 순수 함수 위주로 구성해 테스트와 재사용이 쉽도록 설계했습니다.
// -----------------------------------------------------------------------------
pub mod heuristics {

    // =========================================================================
    // 섹션 1: 기물 가치 (centipawn)
    // =========================================================================

    /// 기물의 센티폰 가치를 반환합니다.
    pub fn get_piece_value(piece: &str) -> i32 {
        match piece {
            "pawn"   => 100,
            "knight" => 320,
            "bishop" => 330,
            "rook"   => 500,
            "queen"  => 900,
            "king"   => 20_000,
            _        => 200,
        }
    }

    // =========================================================================
    // 섹션 2: Piece-Square Table (PST)
    //
    // 좌표 규약 (Board 내부와 동일):
    //   board[y][x], x = 파일(0=a~7=h), y = rank-from-top
    //   y=0 → rank 8 (흑의 홈 / 백의 승진 라인)
    //   y=7 → rank 1 (백의 홈)
    //
    // PST 인덱스 매핑:
    //   백 기물 (is_white=true):  PST[y][x]       (y=0 = 가장 전진한 위치)
    //   흑 기물 (is_white=false): PST[7 - y][x]   (미러링)
    // =========================================================================

    /// 폰 PST: 중앙·전진 가중치. 백 시점 row0=rank8(승진방향), row7=rank1(홈).
    const PAWN_PST: [[i32; 8]; 8] = [
        [  0,  0,  0,  0,  0,  0,  0,  0 ], // rank 8 (승진 직전 행)
        [ 50, 50, 50, 50, 50, 50, 50, 50 ], // rank 7
        [ 10, 10, 20, 30, 30, 20, 10, 10 ], // rank 6
        [  5,  5, 10, 25, 25, 10,  5,  5 ], // rank 5
        [  0,  0,  0, 20, 20,  0,  0,  0 ], // rank 4
        [  5, -5,-10,  0,  0,-10, -5,  5 ], // rank 3
        [  5, 10, 10,-20,-20, 10, 10,  5 ], // rank 2
        [  0,  0,  0,  0,  0,  0,  0,  0 ], // rank 1 (홈 랭크)
    ];

    /// 나이트 PST: 중앙·전진 가중치.
    const KNIGHT_PST: [[i32; 8]; 8] = [
        [-50,-40,-30,-30,-30,-30,-40,-50 ], // rank 8
        [-40,-20,  0,  5,  5,  0,-20,-40 ], // rank 7
        [-30,  5, 10, 15, 15, 10,  5,-30 ], // rank 6
        [-30,  0, 15, 20, 20, 15,  0,-30 ], // rank 5
        [-30,  5, 15, 20, 20, 15,  5,-30 ], // rank 4
        [-30,  0, 10, 15, 15, 10,  0,-30 ], // rank 3
        [-40,-20,  0,  0,  0,  0,-20,-40 ], // rank 2
        [-50,-40,-20,-30,-30,-20,-40,-50 ], // rank 1
    ];

    /// 비숍 PST: 긴 대각선 활용 가중치.
    const BISHOP_PST: [[i32; 8]; 8] = [
        [-20,-10,-10,-10,-10,-10,-10,-20 ], // rank 8
        [-10,  0,  0,  0,  0,  0,  0,-10 ], // rank 7
        [-10,  0,  5, 10, 10,  5,  0,-10 ], // rank 6
        [-10,  5,  5, 10, 10,  5,  5,-10 ], // rank 5
        [-10,  0, 10, 10, 10, 10,  0,-10 ], // rank 4
        [-10, 10, 10, 10, 10, 10, 10,-10 ], // rank 3
        [-10,  5,  0,  0,  0,  0,  5,-10 ], // rank 2
        [-20,-10,-10,-10,-10,-10,-10,-20 ], // rank 1
    ];

    /// 기물-위치 보너스를 센티폰으로 반환합니다.
    ///
    /// - `is_white`: 백이면 `true`, 흑이면 `false`.
    /// - `x`: 파일(0~7), `y`: rank-from-top (0=rank8, 7=rank1).
    pub fn pst_bonus(piece: &str, is_white: bool, x: u8, y: u8) -> i32 {
        let xi = x as usize;
        let yi = if is_white { y as usize } else { (7 - y) as usize };
        match piece {
            "pawn"   => PAWN_PST[yi][xi],
            "knight" => KNIGHT_PST[yi][xi],
            "bishop" => BISHOP_PST[yi][xi],
            _        => 0,
        }
    }

    // =========================================================================
    // 섹션 3: 킹 안전 휴리스틱
    // =========================================================================

    /// 킹 앞 폰 쉴드 1개당 보너스 (최대 3개 = +45cp).
    pub fn pawn_shield_bonus(shield_pawn_count: u8) -> i32 {
        (shield_pawn_count as i32).min(3) * 15
    }

    /// 킹이 중앙 파일에 위치할 때 패널티.
    /// d(3)/e(4) 파일 → 40cp, c(2)/f(5) 파일 → 15cp.
    pub fn king_center_penalty(x: u8) -> i32 {
        match x {
            3 | 4 => 40,
            2 | 5 => 15,
            _     => 0,
        }
    }

    // =========================================================================
    // 섹션 5: 패스트 폰 (Passed Pawn) 휴리스틱
    // =========================================================================

    /// 전진 랭크 수(0~5)에 따른 패스트 폰 보너스 (센티폰).
    /// 폰이 홈 랭크에서 전진한 칸 수를 인수로 받습니다.
    pub fn passed_pawn_rank_bonus(ranks_advanced: u8) -> i32 {
        match ranks_advanced {
            0 => 0,
            1 => 5,
            2 => 10,
            3 => 20,
            4 => 35,
            _ => 60,
        }
    }

    // =========================================================================
    // 섹션 6: 센터 컨트롤 휴리스틱
    // =========================================================================

    /// d4/e4/d5/e5 (x∈{3,4}, y∈{3,4}) 점령 보너스 (+15cp).
    pub fn center_control_bonus(x: u8, y: u8) -> i32 {
        if (x == 3 || x == 4) && (y == 3 || y == 4) {
            15
        } else {
            0
        }
    }

    /// 위치에서 중앙(3.5, 3.5)까지의 맨해튼 거리.
    pub fn center_dist(pos: (u8, u8)) -> i32 {
        (pos.0 as i32 * 2 - 7).abs() + (pos.1 as i32 * 2 - 7).abs()
    }

    // =========================================================================
    // 섹션 7: 수 정렬(Move Ordering) 휴리스틱
    // =========================================================================

    /// MVV-LVA (Most Valuable Victim, Least Valuable Attacker) 캡처 점수.
    /// 피해자 가치 - 공격자 가치 → 큰 기물을 싼 기물로 잡는 수를 우선.
    pub fn score_capture_mvv_lva(attacker: &str, victim: &str) -> i32 {
        get_piece_value(victim) - get_piece_value(attacker)
    }

    /// 프로모션 점수. 퀸 프로모션(≈17900)이 가장 높습니다.
    pub fn score_promotion(promoted_piece: &str) -> i32 {
        8_000 + get_piece_value(promoted_piece)
    }

    /// 킬러 수 점수.
    /// 실제 킬러 테이블 조회는 search 모듈의 `KillerTable::get_bonus`가 담당합니다.
    pub fn score_killer_move(is_killer: bool) -> i32 {
        if is_killer { 9_000 } else { 0 }
    }

    /// 히스토리 휴리스틱 점수.
    /// 실제 누적은 search 모듈의 `HistoryTable`이 담당합니다.
    pub fn score_history_move(history_score: i32) -> i32 {
        history_score
    }

    /// 이동 전후 중앙 거리 차이 기반 점수 (중앙 접근 보너스).
    pub fn score_center_approach(src: (u8, u8), dst: (u8, u8)) -> i32 {
        center_dist(src) - center_dist(dst)
    }

    // =========================================================================
    // 하위 호환 별칭 (기존 engine_huristic API 유지)
    // =========================================================================

    /// 구버전 `score_capture` 별칭 → `score_capture_mvv_lva`로 위임.
    #[inline]
    pub fn score_capture(attacker: &str, victim: &str) -> i32 {
        score_capture_mvv_lva(attacker, victim)
    }

    /// 구버전 `score_center_dist` 별칭 → `center_dist`로 위임.
    #[inline]
    pub fn score_center_dist(pos: (u8, u8)) -> i32 {
        center_dist(pos)
    }
}
